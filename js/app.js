// ── Password Gate ───────────────────────────────────────────────
const PASS = 'egad';
const gate = document.getElementById('gate');

if (gate) {
  function tryUnlock(pw) {
    if (pw.trim().toLowerCase() === PASS) {
      sessionStorage.setItem('synaera_auth', '1');
      gate.classList.add('hidden');
    } else {
      document.getElementById('gate-error').textContent = window._currentGateError || 'Incorrect access code. Please try again.';
      document.getElementById('gate-input').value = '';
      document.getElementById('gate-input').focus();
    }
  }

  if (sessionStorage.getItem('synaera_auth') === '1') {
    gate.classList.add('hidden');
  }

  document.getElementById('gate-btn').addEventListener('click', () => {
    tryUnlock(document.getElementById('gate-input').value);
  });
  document.getElementById('gate-input').addEventListener('keydown', e => {
    if (e.key === 'Enter') tryUnlock(e.target.value);
    document.getElementById('gate-error').textContent = '';
  });
}

// ── Sticky Nav ──────────────────────────────────────────────────
const navbar = document.getElementById('navbar') || document.getElementById('nav');
if (navbar) {
  window.addEventListener('scroll', () => {
    navbar.classList.toggle('scrolled', window.scrollY > 60);
  });
}

// ── Mobile Menu ─────────────────────────────────────────────────
function toggleMenu() {
  const links = document.querySelector('.nav-links');
  if (!links) return;
  const isOpen = links.style.display === 'flex';
  links.style.cssText = isOpen
    ? ''
    : 'display:flex;flex-direction:column;position:absolute;top:100%;left:0;right:0;background:rgba(1,44,54,.98);padding:1.5rem 5%;gap:1rem;backdrop-filter:blur(16px);';
}

const hamburger = document.getElementById('nav-hamburger');
if (hamburger) {
  hamburger.addEventListener('click', toggleMenu);
}

// ── Scroll Reveal ───────────────────────────────────────────────
const observer = new IntersectionObserver((entries) => {
  entries.forEach(e => { if (e.isIntersecting) e.target.classList.add('visible'); });
}, { threshold: 0.12 });
document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

// ── Form Handler (legacy single-page) ───────────────────────────
function handleForm(e) {
  e.preventDefault();
  const btn = e.target.querySelector('.form-submit');
  btn.textContent = i18n[currentLang].form_success;
  btn.style.background = '#16A34A';
  btn.disabled = true;
  setTimeout(() => {
    btn.textContent = i18n[currentLang].form_submit;
    btn.style.background = '';
    btn.disabled = false;
    e.target.reset();
  }, 4000);
}

