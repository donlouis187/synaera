function setLang(lang) {
  if (!i18n[lang]) lang = 'en';
  currentLang = lang;
  localStorage.setItem('synaera_lang', lang);
  document.documentElement.lang = lang;
  // update text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = i18n[lang][el.dataset.i18n];
    if (val !== undefined) el.textContent = val;
  });
  // update html nodes
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = i18n[lang][el.dataset.i18nHtml];
    if (val !== undefined) el.innerHTML = val;
  });
  // update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = i18n[lang][el.dataset.i18nPlaceholder];
    if (val !== undefined) el.placeholder = val;
  });
  // update gate subtitle
  const gateSub = document.querySelector('#gate .gate-logo .sub');
  if (gateSub) gateSub.textContent = (i18n[lang] && i18n[lang].gate_sub) || 'Legal Tech & Document Management';
  // update lang dropdown button label
  const langLabels = { en: 'EN', fr: 'FR', es: 'ES', pt: 'PT' };
  document.querySelectorAll('.lang-select-btn').forEach(btn => {
    btn.innerHTML = (langLabels[lang] || 'EN') + ' <span class="lang-chevron">&#9660;</span>';
  });
  // update active state in all dropdown menus
  document.querySelectorAll('.lang-select-menu button[data-lang]').forEach(btn => {
    btn.classList.toggle('lang-active', btn.dataset.lang === lang);
  });
  // close all lang menus
  document.querySelectorAll('.lang-switcher').forEach(el => el.classList.remove('open'));
  // update form error message handler
  window._currentGateError = (i18n[lang] && i18n[lang].gate_error) || i18n.en.gate_error;
}

function toggleLangMenu(el) {
  const wrap = el.closest('.lang-switcher');
  const wasOpen = wrap.classList.contains('open');
  // close all
  document.querySelectorAll('.lang-switcher').forEach(e => e.classList.remove('open'));
  if (!wasOpen) wrap.classList.add('open');
}

// Close lang dropdown when clicking outside
document.addEventListener('click', function(e) {
  if (!e.target.closest('.lang-switcher')) {
    document.querySelectorAll('.lang-switcher').forEach(el => el.classList.remove('open'));
  }
});

// Initialize language
setLang(currentLang);

// Contact form (Formspree AJAX)
const contactForm = document.getElementById('contact-form');
if (contactForm) {
  contactForm.addEventListener('submit', async function(e) {
    e.preventDefault();
    const data = new FormData(contactForm);
    try {
      const res = await fetch(contactForm.action, { method: 'POST', body: data, headers: { Accept: 'application/json' } });
      if (res.ok) {
        contactForm.style.display = 'none';
        document.querySelector('.form-success').style.display = 'block';
      }
    } catch(err) {}
  });
}

